    public void preencherTabela(String SQL){
        ArrayList dados = new ArrayList();
        
        String []Colunas = new String []{"ID","Nome","CNPJ"};
        
        conn.executaSQL(SQL);
        try {
            conn.rs.first();
            do {dados.add(new Object []{conn.rs.getInt("id_fornecedor"), conn.rs.getString("nome_fornecedor"), conn.rs.getString("cnpj_fornecedor")}); 
            }while (conn.rs.next());   
        } catch (SQLException ex){
            JOptionPane.showMessageDialog(null, "Erro ao preencher tabela!\nERRO:" + ex);
                }
        
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTableTabela.setModel(modelo);
        jTableTabela.getColumnModel().getColumn(0).setPreferredWidth(80);
        jTableTabela.getColumnModel().getColumn(0).setResizable(false);
        jTableTabela.getColumnModel().getColumn(1).setPreferredWidth(380);
        jTableTabela.getColumnModel().getColumn(1).setResizable(false);
        jTableTabela.getColumnModel().getColumn(2).setPreferredWidth(197);
        jTableTabela.getColumnModel().getColumn(2).setResizable(false);
        jTableTabela.getTableHeader().setReorderingAllowed(false);
        jTableTabela.setAutoResizeMode(jTableTabela.AUTO_RESIZE_OFF);
        jTableTabela.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        
    }
