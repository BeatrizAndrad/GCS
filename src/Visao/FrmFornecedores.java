    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:
        if(muda==1){
        mod.setNome(jTextFieldnNome.getText());
        mod.setCNPJ(jFormattedTextFieldCNPJ.getText());
        control.Salvar(mod);
        jTextFieldnNome.setEnabled(false);
        jTextFieldnNome.setText("");
        jFormattedTextFieldCNPJ.setText("");
        jTextFieldID.setText("");
        jButtonSalvar.setEnabled(false);
        jButtonNovo.setEnabled(true);
        jFormattedTextFieldCNPJ.setEnabled(false);
        jButtonCancelar.setEnabled(true);
        preencherTabela("select * from fornecedores order by id_fornecedor");
        }else{
        mod.setNome(jTextFieldnNome.getText());
        mod.setCNPJ(jFormattedTextFieldCNPJ.getText());
        control.Alterar(mod);
        jTextFieldnNome.setEnabled(false);
        jTextFieldnNome.setText("");
        jFormattedTextFieldCNPJ.setText("");
        jTextFieldID.setText("");
        jButtonSalvar.setEnabled(false);
        jButtonNovo.setEnabled(true);
        jFormattedTextFieldCNPJ.setEnabled(false);
        jButtonCancelar.setEnabled(true);
        preencherTabela("select * from fornecedores order by id_fornecedor");
        
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

